#include <stdio.h>
#include <stdlib.h>

int main()
{
    printf("%f",ceil(36.356));      // AKAN MEMBULATKAN BILANGAN KE ATAS
    printf("%f", floor(36.356));    // AKAN MEMBULATKAN BILANGAN KE BAWAH
    printf("%f", pow(4, 3));        // PANGKAT
    printf("%f", sqrt(36));         // AKAR
    return 0;
}
